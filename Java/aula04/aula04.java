// Loops: while, for, do while
package Java.aula04;

public class aula04 {
    public static void main(String[] args){

        for (int i = 0; i <= 3; i++){
            System.out.printf("O valor de i em for é %d\n", i);
        }


        int cont=0;
        while(cont <= 3){
            System.out.printf("O valor de cont em while é %d\n", cont);
            cont++;
        }


    }
}
